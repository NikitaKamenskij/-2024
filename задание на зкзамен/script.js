// Класс для представления спецодежды
class Спецодежда {
    constructor(код, вид, срокНоски, стоимость) {
        this.код = код; // Уникальный код спецодежды
        this.вид = вид; // Вид спецодежды
        this.срокНоски = срокНоски; // Срок носки спецодежды
        this.стоимость = стоимость; // Стоимость спецодежды
    }
}

// Класс для представления цеха
class Цех {
    constructor(код, наименование, начальник) {
        this.код = код; // Уникальный код цеха
        this.наименование = наименование; // Наименование цеха
        this.начальник = начальник; // Начальник цеха
    }
}

// Класс для представления работника
class Работник {
    constructor(код, ФИО, должность, скидка) {
        this.код = код; // Уникальный код работника
        this.ФИО = ФИО; // ФИО работника
        this.должность = должность; // Должность работника
        this.скидка = скидка; // Скидка для работника
    }
}

// Класс для представления получения спецодежды работником
class Получение {
    constructor(кодРаботника, кодСпецодежды, датаПолучения, роспись) {
        this.кодРаботника = кодРаботника; // Код работника
        this.кодСпецодежды = кодСпецодежды; // Код спецодежды
        this.датаПолучения = датаПолучения; // Дата получения спецодежды
        this.роспись = роспись; // Роспись работника
    }
}

// Получение списков данных из localStorage или инициализация пустыми списками
const спецодеждаList = JSON.parse(localStorage.getItem('спецодеждаList')) || [];
const цехаList = JSON.parse(localStorage.getItem('цехаList')) || [];
const работникиList = JSON.parse(localStorage.getItem('работникиList')) || [];
const полученияList = JSON.parse(localStorage.getItem('полученияList')) || [];

// Функция для сохранения данных в localStorage
function saveData() {
    localStorage.setItem('спецодеждаList', JSON.stringify(спецодеждаList));
    localStorage.setItem('цехаList', JSON.stringify(цехаList));
    localStorage.setItem('работникиList', JSON.stringify(работникиList));
    localStorage.setItem('полученияList', JSON.stringify(полученияList));
}

// Функция для добавления новой спецодежды
function addСпецодежда() {
    const вид = document.getElementById('вид-спецодежды').value; // Получение значения вида спецодежды из формы
    const срокНоски = document.getElementById('срок-носки').value; // Получение значения срока носки из формы
    const стоимость = document.getElementById('стоимость').value; // Получение значения стоимости из формы
    const код = спецодеждаList.length + 1; // Генерация нового кода для спецодежды
    const спецодежда = new Спецодежда(код, вид, срокНоски, стоимость); // Создание нового объекта спецодежды
    спецодеждаList.push(спецодежда); // Добавление новой спецодежды в список
    saveData(); // Сохранение данных в localStorage
    displayСпецодежда(спецодежда); // Отображение информации о новой спецодежде
}

// Функция для добавления нового цеха
function addЦех() {
    const наименование = document.getElementById('наименование-цеха').value; // Получение значения наименования цеха из формы
    const начальник = document.getElementById('начальник-цеха').value; // Получение значения начальника цеха из формы
    const код = цехаList.length + 1; // Генерация нового кода для цеха
    const цех = new Цех(код, наименование, начальник); // Создание нового объекта цеха
    цехаList.push(цех); // Добавление нового цеха в список
    saveData(); // Сохранение данных в localStorage
    displayЦеха(цех); // Отображение информации о новом цехе
}

// Функция для добавления нового работника
function addРаботник() {
    const ФИО = document.getElementById('фио-работника').value; // Получение значения ФИО работника из формы
    const должность = document.getElementById('должность-работника').value; // Получение значения должности из формы
    const скидка = document.getElementById('скидка-работника').value; // Получение значения скидки из формы
    const код = работникиList.length + 1; // Генерация нового кода для работника
    const работник = new Работник(код, ФИО, должность, скидка); // Создание нового объекта работника
    работникиList.push(работник); // Добавление нового работника в список
    saveData(); // Сохранение данных в localStorage
    displayРаботники(работник); // Отображение информации о новом работнике
}

// Функция для добавления нового получения спецодежды
function addПолучение() {
    const кодРаботника = document.getElementById('код-работника').value; // Получение значения кода работника из формы
    const кодСпецодежды = document.getElementById('код-спецодежды').value; // Получение значения кода спецодежды из формы
    const датаПолучения = document.getElementById('дата-получения').value; // Получение значения даты получения из формы
    const роспись = document.getElementById('роспись').value; // Получение значения росписи из формы
    const получение = new Получение(кодРаботника, кодСпецодежды, датаПолучения, роспись); // Создание нового объекта получения
    полученияList.push(получение); // Добавление нового получения в список
    saveData(); // Сохранение данных в localStorage
    displayПолучения(получение); // Отображение информации о новом получении
}

// Функция для отображения информации о спецодежде в консоли
function displayСпецодежда(спецодежда) {
    console.log(`Код: ${спецодежда.код}, Вид: ${спецодежда.вид}, Срок носки: ${спецодежда.срокНоски}, Стоимость: ${спецодежда.стоимость}`);
}

// Функция для отображения информации о цехе в консоли
function displayЦеха(цех) {
    console.log(`Код: ${цех.код}, Наименование: ${цех.наименование}, Начальник: ${цех.начальник}`);
}

// Функция для отображения информации о работнике в консоли
function displayРаботники(работник) {
    console.log(`Код: ${работник.код}, ФИО: ${работник.ФИО}, Должность: ${работник.должность}, Скидка: ${работник.скидка}%`);
}

// Функция для отображения информации о получении в консоли
function displayПолучения(получение) {
    console.log(`Код работника: ${получение.кодРаботника}, Код спецодежды: ${получение.кодСпецодежды}, Дата получения: ${получение.датаПолучения}, Роспись: ${получение.роспись}`);
}

// Функция для загрузки и отображения всех данных из localStorage
function loadData() {
    спецодеждаList.forEach(displayСпецодежда); // Отображение всех спецодежд
    цехаList.forEach(displayЦеха); // Отображение всех цехов
    работникиList.forEach(displayРаботники); // Отображение всех работников
    полученияList.forEach(displayПолучения); // Отображение всех получений
}

// Загрузка и отображение данных при запуске скрипта
loadData();
